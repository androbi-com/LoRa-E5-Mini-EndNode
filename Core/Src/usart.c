uint16_t buff2word(uint8_t n)
{
	return (buffer[n] << 8) | buffer[n+1];
}
bool checkBuffer(size_t bufferLen)
{
	uint16_t cksum = buff2word(bufferLen - 2);
	for (uint8_t n=0; n < bufferLen -2; n++)
	{
		cksum -= buffer[n];
	}
	return (cksum == 0);
}
void cleanRX() {
	uint8_t one_byte;
	uint8_t n=0;
	while (HAL_OK == HAL_UART_Receive(&huart2, &one_byte,1,100)) {
		n++;
	}
	return;
}
bool initPMS()
{
	if (HAL_OK !=  HAL_UART_Transmit(&huart2, cfg, PMS_MSG_LEN, 300))
	{
		pms_data[0]=1;
		return false;
	}
	// device sends a 8 byte reply, we just ignore it
	if (HAL_OK !=  HAL_UART_Receive(&huart2, &buffer[0], 8, 1000))
	{
		pms_data[0]=99;
		return false;
	}
	// don't know if this is necessary
	//HAL_Delay(2000);
	pms_data[0]=0;
	return true;
}

bool readPMS()
{
	HAL_StatusTypeDef ret;

	if (HAL_OK !=  HAL_UART_Transmit(&huart2, trg, PMS_MSG_LEN, 300))
	{
		pms_data[0]=2;
		return false;
	}
    ret =  HAL_UART_Receive(&huart2, &buffer[0], 32, 2000);
	if (HAL_OK !=  ret)
	{
		pms_data[0]=3;
		return false;
	}
	if (buff2word(0) != 0x424D) {
		pms_data[0]=4;
		return false;
	}
	size_t bodyLen = buff2word(2);
	if (bodyLen != 28) {
		pms_data[0]=5;
		return false;
	}
	if (!checkBuffer(32))
	{
		pms_data[0]=7;
		return false;
	}
	pms_data[0]=0;
	decodeData();
	return true;
}
void decodeData() {
	uint8_t bin, n;
	for (bin = 0, n= 4; bin < 3; bin ++, n +=2)
	{
		pms_data[bin] = buff2word(n);
	}
}
uint16_t getDataBin(uint8_t n)
{
	return pms_data[n];
}

